def author_identity_patterns() -> tuple[str, ...]:
    first = author_first_lower()
    last = author_last_lower()
    handle = first + last
    return (
        joined_pattern("/", "work", "/", first, "/"),
        joined_pattern("/", "users", "/", first, "/"),
        " ".join([first.title(), last.title()]),
        handle,
        joined_pattern(first, ".", last),
    )



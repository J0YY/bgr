    nvidia_proc = gpu_commands.get("nvidia_proc", {})
    lspci_nvidia = gpu_commands.get("lspci_nvidia", {})
    has_gpu_evidence = (
        isinstance(nvidia_proc, dict)
        and nvidia_proc.get("available") is True
        and bool(nvidia_proc.get("entries"))
    ) or (
        isinstance(lspci_nvidia, dict)
        and "NVIDIA" in str(lspci_nvidia.get("stdout", ""))
    )
    if not has_gpu_evidence:
        raise ValueError("results/environment_v1/gpu_environment.json: missing NVIDIA GPU evidence")

    return ["compute/GPU environment snapshots ok"]



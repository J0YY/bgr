def tex_source_dependency_files(root: Path) -> list[str]:
    dependencies: list[str] = []
    for source_relative in TEX_SOURCE_ARTIFACTS:
        source_path = root / source_relative
        if not source_path.exists():
            continue
        text = source_path.read_text(encoding="utf-8")
        for dependency in re.findall(r"\\input\{([^}]+)\}", text):
            dependencies.append(latex_dependency_path(source_relative, dependency, ".tex"))
        for dependency_group in re.findall(r"\\bibliography\{([^}]+)\}", text):
            for dependency in dependency_group.split(","):
                dependency = dependency.strip()
                if dependency:
                    dependencies.append(latex_dependency_path(source_relative, dependency, ".bib"))
        for package in re.findall(r"\\usepackage(?:\[[^]]*\])?\{([^}]+)\}", text):
            if "/" in package:
                dependencies.append(latex_dependency_path(source_relative, package, ".sty"))
                if package == AUTHOR_KIT_PACKAGE:
                    dependencies.extend(AUTHOR_KIT_ARTIFACTS)
    return sorted(set(dependencies))



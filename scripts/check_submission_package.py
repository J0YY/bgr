def extract_latex_environment(text: str, environment: str) -> str:
    pattern = re.compile(rf"\\begin\{{{re.escape(environment)}\}}(.*?)\\end\{{{re.escape(environment)}\}}", re.S)
    match = pattern.search(text)
    if match is None:
        raise ValueError(f"missing LaTeX {environment} environment")
    return match.group(1)


def latex_word_count(text: str) -> int:
    without_commands = re.sub(r"\\[A-Za-z]+(?:\[[^\]]*\])?(?:\{[^}]*\})?", " ", text)
    without_math = re.sub(r"\$[^$]*\$", " ", without_commands)
    return len(re.findall(r"[A-Za-z0-9]+(?:[-'][A-Za-z0-9]+)*", without_math))



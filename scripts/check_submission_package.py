def csv_mean_perturbed_success_rates(path: Path) -> dict[str, float]:
    rows = read_csv_rows(path)
    grouped: dict[str, list[dict[str, str]]] = {}
    for row in rows:
        perturbation = str(row.get("perturbation", "")).strip()
        if perturbation in {"identity", "none"}:
            continue
        method = str(row.get("method", "")).strip()
        if method:
            grouped.setdefault(method, []).append(row)

    rates: dict[str, float] = {}
    for method, method_rows in grouped.items():
        episodes = [int(row["episodes"]) for row in method_rows if str(row.get("episodes", "")).strip()]
        successes = [int(row["successes"]) for row in method_rows if str(row.get("successes", "")).strip()]
        if episodes and successes and len(episodes) == len(method_rows) and len(successes) == len(method_rows):
            rates[method] = sum(successes) / sum(episodes)
        else:
            rates[method] = sum(float(row["success_rate"]) for row in method_rows) / len(method_rows)
    return rates



def assert_letter_pdf(label: str, path: Path, *, max_pages: int | None = None) -> list[str]:
    if not path.exists():
        raise ValueError(f"{label}: missing {path}")
    metadata = pdf_metadata(path)
    if "612 x 792 pts" not in metadata.page_size:
        raise ValueError(f"{label}: expected letter page size, got {metadata.page_size}")
    if max_pages is not None and metadata.pages > max_pages:
        raise ValueError(f"{label}: expected at most {max_pages} pages, got {metadata.pages}")
    return [
        f"{label}: {metadata.pages} pages, {metadata.page_size}, {metadata.file_size} bytes",
        *check_pdf_metadata_hygiene(label, metadata),
        *check_pdf_font_hygiene(label, path),
    ]


def check_pdf_metadata_hygiene(label: str, metadata: PdfMetadata) -> list[str]:
    info = metadata.info
    identity_fields = ("Title", "Author", "Subject", "Keywords")
    leaked_fields = [field for field in identity_fields if info.get(field, "")]
    if leaked_fields:
        raise ValueError(f"{label}: identity-bearing PDF metadata field(s): {', '.join(leaked_fields)}")

    custom_metadata = info.get("Custom Metadata", "").lower()
    if custom_metadata != "no":
        raise ValueError(
            f"{label}: expected cleared custom PDF metadata, got Custom Metadata={info.get('Custom Metadata', '<missing>')}"
        )
    metadata_stream = info.get("Metadata Stream", "").lower()
    if metadata_stream != "no":
        raise ValueError(
            f"{label}: expected no PDF metadata stream, got Metadata Stream={info.get('Metadata Stream', '<missing>')}"
        )

    feature_checks = {
        "Encrypted": "no",
        "JavaScript": "no",
        "Form": "none",
    }
    unsafe_features = [
        f"{field}={info.get(field, '<missing>')}"
        for field, expected in feature_checks.items()
        if info.get(field, "").lower() != expected
    ]
    if unsafe_features:
        raise ValueError(f"{label}: unsafe PDF metadata feature(s): {', '.join(unsafe_features)}")

    leaks: list[str] = []
    for field, value in info.items():
        field_leaks = double_blind_leaks(value)
        leaks.extend(f"{field}={leak}" for leak in field_leaks)
    if leaks:
        raise ValueError(f"{label}: double-blind leak pattern(s) in PDF metadata: {', '.join(leaks)}")
    return [f"{label}: PDF metadata hygiene ok"]


def check_pdf_font_hygiene(label: str, path: Path) -> list[str]:
    rows = pdf_font_rows(path)
    if not rows:
        raise ValueError(f"{label}: no PDF fonts reported by pdffonts")
    type3 = [row["name"] for row in rows if row["type"] == "Type 3"]
    if type3:
        raise ValueError(f"{label}: Type 3 PDF font(s): {', '.join(type3)}")
    unembedded = [row["name"] for row in rows if row["embedded"] != "yes"]
    if unembedded:
        raise ValueError(f"{label}: unembedded PDF font(s): {', '.join(unembedded)}")
    return [f"{label}: PDF font hygiene ok"]


def first_pdf_page_containing(path: Path, snippet: str, pages: int) -> int | None:
    for page in range(1, pages + 1):
        if snippet in pdf_page_text(path, page):
            return page
    return None


def check_technical_page_limit(path: Path) -> list[str]:
    metadata = pdf_metadata(path)
    checklist_page = first_pdf_page_containing(path, "Reproducibility Checklist", metadata.pages)
    if checklist_page is None:
        raise ValueError(f"{path}: missing rendered Reproducibility Checklist page")
    technical_pages = checklist_page - 1
    if technical_pages > TECHNICAL_PAGE_LIMIT:
        raise ValueError(
            f"{path}: technical content uses {technical_pages} pages before checklist; "
            f"limit is {TECHNICAL_PAGE_LIMIT}"
        )
    if technical_pages < 1:
        raise ValueError(f"{path}: checklist appears before technical content")
    return [f"{path}: technical content uses {technical_pages}/{TECHNICAL_PAGE_LIMIT} pages before checklist"]



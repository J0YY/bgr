def markdown_section(text: str, heading: str) -> str:
    start = text.find(heading)
    if start < 0:
        return ""
    section = text[start + len(heading) :]
    next_heading = section.find("\n## ")
    if next_heading >= 0:
        section = section[:next_heading]
    return section


def backticked_artifact_references(text: str) -> set[str]:
    return {
        reference.split("#", 1)[0].rstrip(".,;:")
        for reference in README_CLAIM_MAP_ARTIFACT_PATTERN.findall(text)
    }


def unpackaged_submission_references(references: list[str], required: set[str]) -> list[str]:
    unpackaged = []
    for reference in references:
        if reference in required:
            continue
        directory_prefix = reference.rstrip("/") + "/"
        if any(required_path.startswith(directory_prefix) for required_path in required):
            continue
        unpackaged.append(reference)
    return unpackaged



def deterministic_zip_info(relative: str, root: Path) -> zipfile.ZipInfo:
    info = zipfile.ZipInfo(relative)
    info.date_time = (1980, 1, 1, 0, 0, 0)
    mode = (root / relative).stat().st_mode & 0o777
    info.external_attr = mode << 16
    return info


def write_submission_zip(root: Path, archive_path: Path) -> int:
    missing = missing_required_files(root)
    if missing:
        raise ValueError(f"cannot build submission archive; missing required artifact(s): {', '.join(missing)}")
    check_required_artifact_scope(root)

    root_resolved = root.resolve()
    archive_resolved = archive_path.resolve()
    required_targets = {root_resolved / relative for relative in required_submission_files()}
    if archive_resolved in required_targets:
        relative = archive_resolved.relative_to(root_resolved)
        raise ValueError(f"cannot build submission archive at required artifact path: {relative.as_posix()}")

    archive_path.parent.mkdir(parents=True, exist_ok=True)
    with zipfile.ZipFile(archive_path, "w", compression=zipfile.ZIP_DEFLATED, compresslevel=9) as archive:
        for relative in required_submission_files():
            archive.writestr(
                deterministic_zip_info(relative, root),
                (root / relative).read_bytes(),
            )
    return len(required_submission_files())


def submission_manifest_payload(root: Path) -> dict:
    missing = [relative for relative in submission_manifest_files() if not (root / relative).exists()]
    if missing:
        raise ValueError(f"cannot build submission manifest; missing required artifact(s): {', '.join(missing)}")

    files = []
    for relative in submission_manifest_files():
        data = (root / relative).read_bytes()
        files.append(
            {
                "path": relative,
                "size_bytes": len(data),
                "sha256": hashlib.sha256(data).hexdigest(),
            }
        )
    return {
        "schema": "bgr-submission-manifest-v1",
        "hash_algorithm": "sha256",
        "files": files,
    }



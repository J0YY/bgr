def check_core_study_artifacts(root: Path) -> list[str]:
    missing: list[str] = []
    seed_errors: list[str] = []
    for label, config_relative, summary_relative, expected_seed_count in CORE_STUDY_ARTIFACTS:
        config_path = root / config_relative
        summary_path = root / summary_relative
        if not config_path.exists():
            missing.append(config_relative)
            configured: set[str] | None = None
        else:
            configured = configured_seeds(config_path)
            if len(configured) != expected_seed_count:
                seed_errors.append(
                    f"{label}: expected {expected_seed_count} configured seeds, found {len(configured)}"
                )
        if not summary_path.exists():
            missing.append(summary_relative)
            continue
        rows = read_csv_rows(summary_path)
        groups = summary_group_seed_sets(rows)
        if not groups:
            seed_errors.append(f"{label}: expected at least one seeded summary group")
            continue
        if configured is None:
            continue
        all_summary_seeds = set().union(*groups.values())
        if all_summary_seeds != configured:
            seed_errors.append(
                f"{label}: summary seeds {sorted(all_summary_seeds)} do not match config seeds {sorted(configured)}"
            )
        mismatched_groups = [
            "/".join(f"{column}={value}" for column, value in group_key) or "all"
            for group_key, seeds in groups.items()
            if seeds != configured
        ]
        if mismatched_groups:
            seed_errors.append(
                f"{label}: result group(s) missing configured paired seeds: {', '.join(mismatched_groups[:5])}"
            )
    if missing:
        raise ValueError(f"missing core study artifact(s): {', '.join(missing)}")
    if seed_errors:
        raise ValueError(f"core study seed-count error(s): {', '.join(seed_errors)}")
    return ["core study configs and paired summary seeds ok"]


